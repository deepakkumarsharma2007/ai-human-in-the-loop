def get_text_and_non_text_parts(parts: List[Union[TextPart, DataPart, FilePart]]) -> tuple:                                
    """
    Separate out text and non text part form parts.
    """
    text_parts = []
    non_text_parts = []
    for part in parts:
        if part.kind == 'text':
            text_parts.append(part)
        else:
            non_text_parts.append(part)
    return text_parts, non_text_parts


def get_non_text_artifacts(non_text_parts: List[Union[DataPart, FilePart]]) -> List[Artifact]:
    """
    Get non text artifacts from given non text parts.
    Add flag to identify response artifacts.
    """
    non_text_artifacts = []
    for non_text_part in non_text_parts:
        if not (non_text_part.kind == 'data' or non_text_part.kind == 'file'):
            continue
        artifact_id = generate_uuid7_id()
        non_text_artifact = Artifact(name=artifact_id, artifactId=artifact_id, parts=[non_text_part], metadata={"source":"agent_response"})
        non_text_artifacts.append(non_text_artifact)
    return non_text_artifacts


def get_text_from_parts(text_parts: List[Union[TextPart]]) -> str:
    """
    Get text from given text parts.
    """
    text_result = ''
    for text_part in text_parts:
        if text_part.kind != 'text':
            continue
        text_result += f' {text_part.text}'
    return str(text_result).strip()

