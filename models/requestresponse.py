# Request Models
class ChatMessage(BaseModel):
    role: str = Field(
        ...,
        description="Role of the message sender, either 'user' or 'agent'.",
        pattern="^(user|agent)$"
    )
    status: Optional[str] = Field(default='',description="Message status")
    id: Optional[str] = Field(
        default_factory=generate_uuid7_id,
        description="Unique identifier for the message, generated by default."
    )
    parts: List[Union[TextPart, FilePart, DataPart]] = Field(
        ...,
        description="Content parts of the message, which can be text, file, or data."
    )
    extensions: List[dict] = Field(
        default_factory=list,
        description="Optional list of extension metadata for the message. will be used to manage document handler or other references or artifacts from the response"
    )

    @field_validator('parts')
    @classmethod
    def check_message_parts(cls, message_parts: List[Union[TextPart, FilePart, DataPart]]) -> List[Union[TextPart, FilePart, DataPart]]:
        """
        Handle validation on parts such that message parts should not be empty.        
        """
        if len(message_parts) == 0:  
            raise PydanticCustomError('parts', 'Message parts can not be empty.', {'input_value': message_parts})

        # @TODO: Remove if current condtion on having first part as text message part removed.
        if message_parts[0].kind != 'text':
            raise PydanticCustomError('parts', 'First message part not text.', {'input_value': message_parts})

        return message_parts


    def get_full_text_message(self):
        if not self.parts:
            return ''
        
        text_messages = []
        for part in self.parts:
            if part.kind != 'text':
                continue
            text_messages.append(part.text)    
        return ' '.join(text_messages)


