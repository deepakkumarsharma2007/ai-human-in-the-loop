"""
Models for chat request and response handling in the Orchestrator API.
Classes:
    ChatMessage:
        Represents a single chat message sent by either a user or an agent.
        Fields:
            role (str): Role of the message sender, either 'user' or 'agent'.
            id (Optional[str]): Unique identifier for the message, generated by default.
            parts (List[Union[TextPart, FilePart, DataPart]]): Content parts of the message, which can be text, file, or data.
            extensions (List[dict]): Optional list of extension metadata for the message.
    OrchestratorChatRequest:
        Represents a chat request to the orchestrator.
        Fields:
            conversationid (Optional[str]): Identifier for the conversation.
            message (Optional[ChatMessage]): The chat message being sent.
    ChatRequestErrorResponse:
        Represents an error response for a chat request.
        Fields:
            conversationid (Optional[str]): Identifier for the conversation related to the error.
            messageid (Optional[str]): Identifier for the message related to the error.
            error (str): Error message description.
            code (Optional[str]): Optional error code.
            details (Optional[dict]): Additional details about the error.
    ConversationMessage:
        Represents a chat message within a conversation, including creation time.
        Fields:
            createddatetime (datetime): Timestamp when the message was created.
            (Inherited fields from ChatMessage)
    ConversationResponse:
        Represents a response containing multiple conversation messages.
        Fields:
            messages (List[ConversationMessage]): List of messages in the conversation.
    AllConversationResponseItem:
        Represents summary information for a conversation.
        Fields:
            conversationid (str): Unique identifier for the conversation.
            conversationname (str): Name of the conversation.
            lastupdateddate (str): Timestamp of the last update to the conversation.
"""
